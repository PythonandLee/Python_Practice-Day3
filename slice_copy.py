# Use '[:]' to copy list. 
my_fruits = ['kiwi', 'tomato', 'orange', 'apple']
friend_fruits = my_fruits[:]

# Use 'append()' to add elements at the end,
# It can be appended seperately to different lists.
my_fruits.append('peach')
print('I like many fruits such as:')
print(my_fruits)

print('\nMy friend also like fruits such as:')
print(friend_fruits)
